// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(num, num2) {
  //if num is bigger than second num, return second num. return 1st otherwise
  return num>num2 ? num2 : num;
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(num) {
//return true if/when num hits 0 (meaning its even)
  if(num === 0)
    return true;
//return false if/when num hits 1 (meaning its odd)
  if(num === 1)
    return false;
  else{
     //if num is positive (or 0), keep subtracting 2 until it is 0 or 1.
     if(num > -1){
      num -= 2;
      return isEven(num);
     }else{
      //if num is negative, keep adding 2 until it is 0 or 1.
      num += 2;
      return isEven(num);
     }
     
  }
  
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(str, char) {
  //loop through string, make a count variable
  for(var i = 0, count = 0; i < str.length; i++){
    //increment count when element is equal to the given character
    if(char === str[i])
      count++;
  }
  return count;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(str) {
  //call countchars with b argument
  
  return countChars(str, 'B');
}

