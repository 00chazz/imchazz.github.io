//taka 3 num params, if num 1 is less than num 2 then num 3 = 1, -1 if otherwise
function range(num1, num2, num3 = num1 < num2 ? 1 : -1) {
  var arr = [];
  // 2 numbers arent the same then find the range.
  if(num1 !== num2){
    //if num2 is bigger than num1, it will increment by +1 (go up), if not, will decrement by -1 (go down)
    if (num3 > 0) {
      for (let i = num1; i <= num2; i += num3) arr.push(i);
    } else {
      for (let i = num1; i >= num2; i += num3) arr.push(i);
    }
  }
  return arr;
}

